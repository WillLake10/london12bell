<h1>2013 Competition</h1>

<p>The 2013 competition will be hosted by the University of London Society, taking place on Saturday 26th October at St Magnus the Martyr, Lower Thames Street, by kind permission of the rector, The Rev Philip Warner and tower keeper, Dickon Love. The chief judge is Martin Whiteley, assisted by Maggie Whiteley and Liz Orme.</p>

<h3>Draw and ringing.</h3>
<p>The draw will take place at 10:45, and photographs of the bands will be taken immediately afterwards. The first band will ring at 11:30. Bands will ring at twenty minute intervals, with a twenty minute break at 13:10. It is expected that the results will announced at 16:30.</p>

<h3>Refreshments</h3>
<p>Refreshments including tea, coffee, and bacon rolls will be available from 10am for as long as demand lasts.</p>

<p>The pub for the day is The Monument, 18 Fish Street Hill, EC3R 6DB, just a short walk from the church. This will be open from 11:00 until 20:00. Food will be available from 12:00 to 14:00 when the chef is there especially for us.</p>

<h3>The Competition Touch</h3>
<p>The test piece is 6 leads of Cambridge Surprise Maximus brought round by plain hunting at the 6th lead end, the last four leads only to be judged.</p>

