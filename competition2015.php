<h1>2015 Competition</h1>

<p>The 2015 competition will take place on Saturday 24th October at St Giles Cripplegate, by kind permission of the rector, the Rev Katharine Rumens and tower secretary, Gwen Rogers. The chief judge is Andrew Mills, assisted by his wife Sharon.</p>

<h3>Draw and ringing.</h3>
<p>The welcome and draw will take place at 10:30, and photographs of the bands will be taken immediately afterwards. The first band will ring at 11:30. Ringing will finish at 15:10, with results in the Butcher's Hook and Cleaver at around 16:30.</p>

<h3>Food and Drink</h3>
<p>The pub for the day is the Butchers Hook and Cleaver, on W Smithfield. The pub is opening especially for us.</p>

