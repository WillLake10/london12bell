<h1>2010 Competition</h1>
<p>The 2010 competition will take place on Saturday 30th October at St Mary le Bow. Arrangements for the day have been made by Simon Meyer, the Steeplekeeper.</p>

<h3>Draw and ringing.</h3>
<p>The draw will take place in the church at 11am with the first band ringing at 11:30. The results will be announced in church at 4:30pm. The bells will be available for open ringing between the end of the competition &amp; the results and Simon will be happy to open the tower up to the roof if anyone would like to take in the view (and if the weather cooperates).</p>

<h3>Refreshments</h3>
<p>The pub venue for the day will be The Fine Line diagonally across the churchyard. This should be an excellent location from which to hear the bells in comfortable surroundings which will just have been refurbished a few weeks before the competition. Teas, coffees, bacon rolls etc will be available here from 10am and a selection of food &amp; real ale will be available during the day. Simon would encourage people to use this venue as they are opening especially for us, including bringing in extra supplies of beer. Toilets will be available both in The Fine Line &amp; the church.</p>

<h3>Transport</h3>
<p>Parking in central London is not recommended but St Mary le Bow is just a couple of minutes walk from Bank underground (the church is actually signposted in the station), Cannon Street &amp; City Thameslink stations with Liverpool Street being just a little further away.</p>

<h3>Judges</h3>
<p>The chief judge this year is Rick Shallcross who will be supported by Tom Griffiths. Adrian Udal will be recording the pieces to assist the judges in their deliberations.</p>

<h3>The Competition Touch</h3>
<p>The touch for the competition is 255 Stedman Cinques. The first eleven sixes will not be marked, with marking commencing as the sixth leaves the back.</p>
