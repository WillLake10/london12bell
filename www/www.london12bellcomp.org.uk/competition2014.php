<h1>2014 Competition</h1>

<p>The 2014 competition will take place on Saturday 25th October at St Mary-le-Bow, by kind permission of the rector, The Rev George Bush and steeple keeper, Simon Meyer. The chief judge is David Dearnley, assisted by Jennifer Town and David House.</p>

<h3>Draw and ringing.</h3>
<p>The draw will take place at 10:45, and photographs of the bands will be taken immediately afterwards. The first band will ring at 11:30. Ringing will finish at 15:10, with results in the church at around 16:30.</p>

<h3>Food and Drink</h3>
<p>The pub for the day is the Fine Line, on Bow Churchyard. Bacon butties will be on sale from 10am, and the pub is opening especially for us.</p>

