<h1>2011 Competition</h1>

<img class="intext" src="images/shoreditch2.jpg" title="Shoreditch" style="height: 350px" />

<p>The 2011 competition will take place on Saturday 22nd October at St Leonard's Shoreditch by kind invitation of the Society of Royal Cumberland Youths and enthusiastic permission of the Vicar, the Revd. Paul Turp. The chief judge this year is William R Haynes.</p>

<h3>Draw and ringing.</h3>
<p>The draw will take place at 11am with the first band ringing at 11:30. Bands will ring at twenty minute intervals, with a twenty minute break at 13:10. It is expected that the results will announced at 16:30.</p>

<h3>Refreshments</h3>
<p>Tea, coffee, and bacon sandwiches will be available in the church hall (accessed via the church) from 10am until 1pm. Tea, coffee and cakes will be available during the afternoon. A beer tent will be open from immediately after the draw until the end of the competition. Drinking after this will move to the White Hart on the corner of Bishopsgate and Liverpool Street. Please help finish the beer and cider at the church first! The pub does food in to the evening and has up to 8 ales on tap. There is seating downstairs as well as at ground level, so please make use of both levels.</p>

<h3>The Competition Touch</h3>
<p>The test piece is 6 leads of Cambridge Surprise Maximus, coming round by plain hunting at the final lead-end. The last four leads of the test piece will be judged.</p>

<h3>Competing Bands</h3>
<p>The bands taking part in this year's competition are:</p>
<ul>
<li>Ancient Society of College Youths</li>
<li>Middlesex County Association &amp; London Diocesan Guild</li>
<li>Society of Royal Cumberland Youths</li>
<li>South Croydon</li>
<li>Southwark (two bands)</li>
<li>St Martin-in-the-Fields</li>
<li>St. Michael's Cornhill</li>
<li>University of London Society of Change Ringers</li>
</ul>
