<h1>2012 Competition</h1>

<p>The 2012 competition will take place on Saturday 27th October at Croydon Minster. The chief judge is Robert Newton, assisted by Patricia Newton and Tom Hinks.</p>

<h3>Draw and ringing.</h3>
<p>The draw will take place at 10:45, and photographs of the bands will be taken immediately afterwards. The first band will ring at 11:30. Bands will ring at twenty minute intervals, with a twenty minute break at 13:10. It is expected that the results will announced at 16:30.</p>

<h3>Refreshments</h3>
<p>Refreshments including tea, coffee, and bacon rolls will be available during the day.</p>

<p>The hall will be open from 9:30 for tea, coffee, and bacon rolls. The beer tent will open at 11am, and barbecue food will be available from noon. From 5pm socialising will move to the Green Dragon, where an upstairs room is booked, and there will be a 15% discount on food and beer for ringers.</p>

<h3>The Competition Touch</h3>
