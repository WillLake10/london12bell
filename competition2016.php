<h1>2016 Competition</h1>

<p>The 2016 competition will take place on Saturday 22nd October at St Sepulchre-without-Newgate. The judges are Linda Garton, John Loveless, and Simon Read.</p>

<h3>Draw and ringing.</h3>
<p>The welcome and draw will take place at 10:30, and photographs of the bands will be taken immediately afterwards. The first band will ring at 11:00. Ringing will finish at 14:30, with results in the Butcher's Hook and Cleaver at around 15:30.</p>

<h3>Food and Drink</h3>
<p>The pub for the day is the Butchers Hook and Cleaver, on W Smithfield. It will be open from 11:00, serving four real ales and food.</p>

<h3>The Competition Touch</h3>
<p>The test piece is 6 leads of Cambridge Surprise Maximus, finishing with a twelfths place lead end. All of the rows of the touch and the first row of rounds at the end will be judged.</p>
 
<h3>Competing Bands</h3>
<p>The bands taking part in this year's competition are:</p>
